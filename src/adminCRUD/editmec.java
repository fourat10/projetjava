    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        String cin_user =cin_field.getText();
        String telp =tel.getText();
        String address =addresse.getText();
        String mail =email.getText();
        if(telp.equals("") && address.equals("") && mail.equals(""))
        {
            JOptionPane.showMessageDialog(null, "tous les champs sont vides", "Error", JOptionPane.ERROR_MESSAGE);

        }
        else
        {
            if(!(telp.equals("")))
            {
                String query=" UPDATE mecanicien SET tel='"+telp+" 'WHERE CIN='"+cin_user+"'";
                Statement stmt;

                try
                {
                    stmt = con.createStatement();
                    stmt.execute(query);
                }
                catch (SQLException ex)
                {
                    Logger.getLogger(updateclient.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if(!(address.equals("")))
            {
                String query1=" UPDATE mecanicien SET addresse='"+address+"'WHERE CIN='"+cin_user+"'";
                Statement stmt1;
                try
                {
                    stmt1 = con.createStatement();
                    stmt1.execute(query1);
                }
                catch (SQLException ex)
                {
                    Logger.getLogger(updateclient.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if(!(mail.equals("")))
            {
                if(!(isValidEmailAddress(mail)))
                {
                    JOptionPane.showMessageDialog(null, "address mail invalide !!!!!!", "Error", JOptionPane.ERROR_MESSAGE);
                    if(!(address.equals(""))|!telp.equals(""))
                        JOptionPane.showMessageDialog(null," MECANICIEN MIS A JOUR");
                    
                }
                else
                {
                    String query2=" UPDATE mecanicien SET email='"+mail+"' WHERE CIN='"+cin_user+"'";
                    Statement stmt2;

                    try
                    {
                        stmt2 = con.createStatement();
                        stmt2.execute(query2);
                    }
                    catch (SQLException ex)
                    {
                        Logger.getLogger(updateclient.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    JOptionPane.showMessageDialog(null," MECANICIEN MIS A JOUR");
                    
                }
            }
            cin_field.setText("");
            
        }
        load();
    }//GEN-LAST:event_editActionPerformed

